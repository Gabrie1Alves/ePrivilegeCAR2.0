function respostaPergunta1(){
    var respostas = document.getElementById("r1");
    var simbolo = document.getElementById("s1");

    if(respostas.style.display == "none"){
        respostas.style.display = "block";
        simbolo.innerHTML = "▴";
    }else{
        respostas.style.display = "none";
        simbolo.innerHTML = "▾";
    }
}

